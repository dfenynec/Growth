 <script>
document.addEventListener('DOMContentLoaded', function() {
  var form = document.getElementById('contactForm');
  var responseDiv = document.getElementById('formResponse');
  if (!form) return;

  form.addEventListener('submit', function(e) {
    e.preventDefault();
    var formData = new FormData(form);

    // reCAPTCHA v2: get token from widget
    if (typeof grecaptcha !== 'undefined') {
      var recaptchaToken = grecaptcha.getResponse();
      if (!recaptchaToken) {
        responseDiv.innerHTML = '<div class="alert alert-danger">Prosím, potvrďte reCAPTCHA.</div>';
        return;
      }
      formData.append('g-recaptcha-response', recaptchaToken);
    }

    fetch(form.action, {
      method: 'POST',
      body: formData
    })
    .then(res => res.json())
    .then(data => {
      responseDiv.innerHTML = '<div class="' + data.alert + '">' + data.message + '</div>';
      if (typeof grecaptcha !== 'undefined') grecaptcha.reset();
      form.reset();
    })
    .catch(() => {
      responseDiv.innerHTML = '<div class="alert alert-danger">Došlo k chybě při odesílání formuláře.</div>';
    });
  });
});
</script>
